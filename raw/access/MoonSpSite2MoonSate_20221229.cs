    public void MoonSpSite2MoonSate_20221229()
    {
        //  读取json文件，并序列化为类对象
        string inputStr = """
            {
              "Description": "月球南极附近 点-月球极轨卫星 Access, 无约束(默认月遮挡)",
              "Start": "2022-12-29T04:00:00Z",
              "Stop": "2022-12-31T04:00:00Z",
              "ComputeAER": true,
              "FromObjectPath": {
                "Name": "moonSouthPoleSite",
                "Position": {
                  "$type": "SitePosition",
                  "CentralBody": "Moon",
                  "cartographicDegrees": [ -75, -89.5, 0 ]
                }
              },

              "ToObjectPath": {
                "Name": "moonSate_inc_90deg",
                "Position": {
                  "$type": "TwoBody",
                  "CentralBody": "Moon",
                  "GravitationalParameter": 4.90280030555540e+012,
                  "OrbitEpoch": "29 Dec 2022 04:00:00.000000",
                  "CoordSystem": "Inertial",
                  "CoordType": "Classical",
                  "OrbitalElements": [ 2037400, 0, 90, 0, 0, 0 ]
                }
              }
            }
            """;
        var input = JsonSerializer.Deserialize<AccessInput2>(inputStr);

        //  Access计算
        var output = AccessComputeV2.Compute(input);
               
        
        Assert.IsTrue(output.IsSuccess);
        Console.WriteLine(output.ToString());
        //  与STK的结果对比
        double ebsl = 0.01;
        Assert.AreEqual(1441.870, output.Passes[0].Duration, ebsl);
        Assert.AreEqual(1441.868, output.Passes[1].Duration, ebsl);
        Assert.AreEqual(1441.865, output.Passes[2].Duration, ebsl);


