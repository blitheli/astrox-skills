        [TestMethod()]
        public void LightingTimes_MoonBruno_250518()
        {
            //  读取json文件，并序列化为类对象
            string inputStr = """
                {
                  "Description": "Moon Bruno坑地一点的光照,考虑地形遮罩数据",
                  "Start": "2022-09-05T04:00:00Z",
                  "Stop": "2023-09-04T04:00:00Z",

                  "Position": {
                    "$type": "SitePosition",
                    "CentralBody": "Moon",
                    "cartographicDegrees": [ 102.91745, 35.911758, -2345.2 ],
                    "clampToGround": true
                  }
                }
                """;
            var input = JsonSerializer.Deserialize<LightingTimesInput>(inputStr);

            //  Access计算            
            var output = LightCompute.LightingTimes(input);

            if (!output.IsSuccess)
            {
                Console.WriteLine(output.Message);
                Assert.Fail();
            }
            Console.WriteLine(output.ToString());

            //  与STK对比，小于0.1s
            var sunLight = output.SunLight.Intervals;
            double ebsl = 0.1;
            Assert.AreEqual(398451.843, sunLight[0].Duration, ebsl);
            Assert.AreEqual(1279731.325, sunLight[1].Duration, ebsl);
            Assert.AreEqual(1273503.127, sunLight[2].Duration, ebsl);
            Assert.AreEqual(1266417.897, sunLight[3].Duration, ebsl);
            Assert.AreEqual(1260604.296, sunLight[4].Duration, ebsl);
            Assert.AreEqual(1257853.102, sunLight[5].Duration, ebsl);
            Assert.AreEqual(1258952.578, sunLight[6].Duration, ebsl);
            Assert.AreEqual(1263441.604, sunLight[7].Duration, ebsl);
            Assert.AreEqual(1269883.286, sunLight[8].Duration, ebsl);
            Assert.AreEqual(1276417.418, sunLight[9].Duration, ebsl);
            Assert.AreEqual(1281292.951, sunLight[10].Duration, ebsl);
            Assert.AreEqual(1283252.646, sunLight[11].Duration, ebsl);
            Assert.AreEqual(1281778.446, sunLight[12].Duration, ebsl);

            var penUmbra = output.Penumbra.Intervals;
            Assert.AreEqual(4623.909, penUmbra[0].Duration, ebsl);
            Assert.AreEqual(4652.420, penUmbra[1].Duration, ebsl);
            Assert.AreEqual(4661.844, penUmbra[2].Duration, ebsl);

            var umbra = output.Umbra.Intervals;
            Assert.AreEqual(1258109.234, umbra[0].Duration, ebsl);
            Assert.AreEqual(1266398.237, umbra[1].Duration, ebsl);
            Assert.AreEqual(1276348.817, umbra[2].Duration, ebsl);
        }
