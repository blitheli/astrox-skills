        [TestMethod()]
        public void LightingTimes_EarthJ2_250429()
        {
            //  读取json文件，并序列化为类对象
            string inputStr = """
                {
                  "Description": "SateJ2的光照计算,默认地球遮挡",
                  "Start": "2022-09-05T04:00:00Z",
                  "Stop": "2022-09-06T04:00:00Z",

                  "Position":{
                    "$type": "J2",
                    "OrbitEpoch": "5 Sep 2022 04:00:00.00",
                    "CoordSystem": "Inertial",
                    "CoordType": "Classical",
                    "OrbitalElements": [ 7678140, 0, 58.5, 0, 0, 0 ]
                  }
                }
                """;
            var input = JsonSerializer.Deserialize<LightingTimesInput>(inputStr);

            //  Lighting计算
            var output = LightCompute.LightingTimes(input);
                     
            if (!output.IsSuccess)
            {
                Console.WriteLine(output.Message);
                Assert.Fail();
            }

            Console.WriteLine(output.ToString());
            var sunLight = output.SunLight.Intervals;
            double ebsl = 0.1;
            Assert.AreEqual(4627.321, sunLight[0].Duration, ebsl);
            Assert.AreEqual(4626.374, sunLight[1].Duration, ebsl);
            Assert.AreEqual(4625.454, sunLight[2].Duration, ebsl);
            Assert.AreEqual(4624.551, sunLight[3].Duration, ebsl);
            Assert.AreEqual(4623.670, sunLight[4].Duration, ebsl);
            Assert.AreEqual(4622.810, sunLight[5].Duration, ebsl);
        }

