        [TestMethod()]
        public void LightingTimes_MarsTwoBody_230409()
        {
            //  读取json文件，并序列化为类对象
            string inputStr = """
                {
                  "Description": "TwoBody的光照计算, 火星遮挡",
                  "Start": "7 Apr 2023 04:00:00.00000",
                  "Stop": "8 Apr 2023 04:00:00.00000",

                  "Position": {
                    "$type": "TwoBody",
                    "CentralBody": "Mars",
                    "GravitationalParameter": 4.282837564100000e+013,
                    "OrbitEpoch": "7 Apr 2023 04:00:00.00000",
                    "CoordSystem": "Inertial",
                    "CoordType": "Classical",
                    "OrbitalElements": [ 3696190, 0, 45, 0, 0, 0 ]
                  }
                }
                """;
            var input = JsonSerializer.Deserialize<LightingTimesInput>(inputStr);

            //  Access计算            
            var output = LightCompute.LightingTimes(input);


            if (!output.IsSuccess)
            {
                Console.WriteLine(output.Message);
                Assert.Fail();
            }

            Console.WriteLine(output.ToString());
            //  与STK结果对比
            double ebsl = 0.2;
            Assert.AreEqual(1102.403, output.SunLight.Intervals[0].Duration, ebsl);
            Assert.AreEqual(5793.213, output.SunLight.Intervals[1].Duration, ebsl);
            Assert.AreEqual(5795.141, output.SunLight.Intervals[2].Duration, ebsl);
            Assert.AreEqual(5797.070, output.SunLight.Intervals[3].Duration, ebsl);


            Assert.AreEqual(29.109, output.Penumbra.Intervals[0].Duration, ebsl);
            Assert.AreEqual(29.196, output.Penumbra.Intervals[1].Duration, ebsl);
            Assert.AreEqual(29.177, output.Penumbra.Intervals[2].Duration, ebsl);

            Assert.AreEqual(973.402, output.Umbra.Intervals[0].Duration, ebsl);
            Assert.AreEqual(971.337, output.Umbra.Intervals[1].Duration, ebsl);
            Assert.AreEqual(969.276, output.Umbra.Intervals[2].Duration, ebsl);
        }

