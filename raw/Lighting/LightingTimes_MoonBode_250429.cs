        [TestMethod()]
        public void LightingTimes_MoonBode_250429()
        {
            //  读取json文件，并序列化为类对象
            string inputStr = """
                {
                  "Description": "Moon 博得月溪点的光照,无地形遮罩，但是有月食",
                  "Start": "2029-06-01T04:00:00.000Z",
                  "Stop": "2029-07-01T04:00:00.000Z",

                  "Position": {
                    "$type": "SitePosition",
                    "CentralBody": "Moon",
                    "cartographicDegrees": [ -4.8, 11, 0 ],
