            Assert.AreEqual(3779.388, penUmbra[1].Duration, ebsl);
            Assert.AreEqual(3470.133, penUmbra[2].Duration, ebsl);
            Assert.AreEqual(3476.487, penUmbra[3].Duration, ebsl);
