        [TestMethod()]
        public void E2M_DeltaDecRA_250328()
        {
            //  输入json文件的路径
            string filePath0 = new DirectoryInfo(Assembly.GetExecutingAssembly().Location).Parent.FullName;
            filePath0 = Path.Combine(filePath0, @"Astrogator/Target");

            //  读取输入参数(json)
            string fp = Path.Combine(filePath0, "E2M_DeltaDecRA_250328.json");

            //  读取json文件，并序列化为类对象
            string inputStr = File.ReadAllText(fp, Encoding.UTF8);
            var input = JsonSerializer.Deserialize<AstrogatorMCS>(inputStr);

            //  调用webApi            
            var output = input.RunMCS();

            if (!output.IsSuccess)
                Assert.Fail(output.Message);

            //  mcs结果序列化输出
            Console.WriteLine(JsonSerializer.Serialize(output,
                new JsonSerializerOptions
                {
                    WriteIndented = true,
                    Encoder = System.Text.Encodings.Web.JavaScriptEncoder.UnsafeRelaxedJsonEscaping
                }));

            /*
              "RightAscension": 208.5119502664131,
              "Declination": 1.6833635340305373,

                "Delta_Declination": -0.0066719464977094325,
            "Delta_Right_Asc": -0.009282618908995992,
            "Altitude": -0.0001821991004568405,
            "TrueAnomaly": -103.61409386636434
          }
             */
            var seg = output.MainSequenceResults[0] as MCSTargetSequenceResults;
            var seg0 = seg.SegmentResults[0];
            //  初始段
            Assert.AreEqual(208.5119502664131, seg0.InitialState.Spherical.RightAscension, 1e-4);
            Assert.AreEqual(1.6833635340305373, seg0.InitialState.Spherical.Declination, 1e-4);            
            Assert.AreEqual(6678137.000000001, seg0.InitialState.Spherical.RadiusMagnitude, 1e-4);
            Assert.AreEqual(0, seg0.InitialState.Spherical.HorizFPA, 1e-13);
            Assert.AreEqual(61.5, seg0.InitialState.Spherical.VelocityAzimuth, 1e-13);
            Assert.AreEqual(10900.0, seg0.InitialState.Spherical.VelocityMagnitude, 1e-6);
            
            //  最后一段
            var seg3 = seg.SegmentResults[2];
            Assert.AreEqual(-0.0066719464977094325, (double)seg3.Results["Delta_Declination"], 1e-5);
            Assert.AreEqual(-0.009282618908995992, (double)seg3.Results["Delta_Right_Asc"], 1e-5);
            Assert.AreEqual(-0.0001821991004568405, (double)seg3.Results["Altitude"], 1e-3);
            Assert.AreEqual(-103.61409386636434, (double)seg3.Results["TrueAnomaly"], 1e-3);
   
        }

