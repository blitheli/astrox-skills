         测试 Astrogator 地球霍曼转移轨道
         
          一个典型的霍曼转移任务，目标是将航天器从初始轨道（近地点约8000km）转移到一个远地点约42000km、偏心率为0.1的椭圆轨道。整个过程包括在近地点进行第一次加速机动，进入转移轨道后在远地点进行第二次机动以调整最终轨道的偏心率。

        # 飞行任务各段简要说明

        ## 主序列飞行段

        1. **Initial_State（初始状态）**
          - 初始化航天器在地心惯性坐标系中的位置和速度
          - 初始位置：(8000000, 0, 0) 米
          - 初始速度：(1500, 7500, 0) 米/秒
          - 设置航天器物理参数：干质量100kg，燃料900kg
          - 结果计算：远地点距离（RadiusOfApoapsis）

        2. **Propagate_To_Perigee（传播至近地点）**
          - 使用地球点质量模型传播
          - 停止条件：真近点角为0（近地点位置）
          - 目的：保证在近地点位置开始霍曼转移

        3. **Inner_Target_List（目标序列段）**
          - 包含霍曼转移的主要机动序列
          - 子段：
            
            a. **First_Maneuver（第一次脉冲机动）**
            - 在VNC坐标系下施加脉冲，初始值(200, 0, 0) m/s
            - 机动目的：提高轨道能量，进入转移轨道
            
            b. **Transfer_Orbit（转移轨道传播）**
            - 使用地球点质量模型传播
            - 停止条件：真近点角为180度（远地点位置）
            - 结果计算：远地点距离（RadiusOfApoapsis）
            - 目的：传播至转移轨道的远地点
            
            c. **Second_Maneuver（第二次脉冲机动）**
            - 在VNC坐标系下施加脉冲，初始值(500, 0, 0) m/s
            - 结果计算：偏心率（Eccentricity）
            - 目的：环形化轨道，达到目标轨道高度

        ## 目标求解配置

        **Solve_For_Hohmann_Transfer（霍曼转移微分校正器）**
        输出优化过程
        - 控制变量：
          - 第一次机动X方向脉冲大小(ImpulsiveMnvr.Cartesian.X)
          - 第二次机动X方向脉冲大小(ImpulsiveMnvr.Cartesian.X)
        - 约束条件：
          - 转移轨道远地点距离 = 42000000.0 米
          - 最终轨道偏心率 = 0.1


        结果:
                - 微分修正从初值开始；9次收敛
        */
