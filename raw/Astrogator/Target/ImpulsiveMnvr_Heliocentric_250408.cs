            //  最后一段
            var seg = output.MainSequenceResults[2] as MCSTargetSequenceResults;
            var dcResult = seg.OperatorResults[0] as TargetOperatorDifferentialCorrectorResults;
            var seg1 = seg.SegmentResults[1];

            //  脉冲结果
            //  与STK的结果对比(dv: 0.0003m/s)
            Assert.AreEqual(-132.2725057870098, dcResult.ControlParameters[0].Values.Last(), 0.0001);
            Assert.AreEqual(72.88363916368178, dcResult.ControlParameters[1].Values.Last(), 0.0001);
            Assert.AreEqual(-12.19795044008312, dcResult.ControlParameters[2].Values.Last(), 0.0003);
            //  最后一段(与STK对比)
            Assert.AreEqual(-1.4975332215331161e+08, (double)seg1.Results["X"] * 0.001, 5);
            Assert.AreEqual(-3.5387463908204824e+07, (double)seg1.Results["Y"] * 0.001, 6);
            Assert.AreEqual(-1.5950655382286744e+07, (double)seg1.Results["Z"] * 0.001, 5);
